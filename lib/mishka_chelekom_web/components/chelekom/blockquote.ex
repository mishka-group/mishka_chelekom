  def blockquote(assigns) do
    ~H"""
    <figure
      class={[
        space_class(@space),
        border_class(@border),
        color_variant(@variant, @color),
        rounded_size(@rounded),
        padding_size(@padding),
        @font_weight,
        @class
      ]}
    >
      <.blockquote_icon />
      <blockquote class="p-2 italic">
        <%= render_slot(@inner_block) %>
      </blockquote>
      <figcaption
        :for={caption <- @caption}
        class={[
          "flex items-center justify-center space-x-3 rtl:space-x-reverse",
          caption_position(caption[:position])
        ]}
      >
        <img
          :if={!is_nil(caption[:image])}
          class={["w-6 h-6 rounded-full", caption[:image_class]]}
          src={caption[:image]}
        />
        <div class="flex items-center divide-x-2 rtl:divide-x-reverse divide-gray-500">
          <%= render_slot(caption) %>
        </div>
      </figcaption>
    </figure>
    """
  end

